print(a[:27]) #String ifadeden istediğimiz harfe kadar almak;
print(a[::2]) #String ifadeden 2'şer 2'şer harf almak;
print(a[2:25:3]) #String ifadeden belirli harften başlayıp  3'er 3'er harf almak;
